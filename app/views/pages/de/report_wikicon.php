		<div class="limit--s">
			<h1 class="section__title h--beta">
				<em>Verein und Communitys</em><br>Größte WikiCon in Kornwestheim
			</h1>
			<div class="section__teaser t--gamma">
				Bei Wikipedia mitzuarbeiten bedeutet nicht, einsam vor dem eigenen Rechner Artikel zu schreiben. Die Wikipedia-Community ist eine Gemeinschaft, die sich auch in der realen Welt regelmäßig trifft. Bei keiner anderen Veranstaltung kommen so viele Wikipedianerinnen und Wikipedianer zusammen wie bei der WikiCon: Die größte deutschsprachige Konferenz für Freiwillige der Wikimedia-Projekte findet einmal im Jahr an wechselnden Orten statt, 2016 in der Region Stuttgart.
			</div>
			<div class="section__body t--gamma-serif">
				<p>
					Die WikiCon wird von Freiwilligen für Freiwillige organisiert. Die Mitarbeitenden von Wikimedia Deutschland, Wikimedia Österreich und Wikimedia Schweiz unterstützen das Organisationsteam dabei. Neben Projektvorstellungen sowie Vorträgen und Diskussionen zu allen möglichen Themen rund um die Wikimedia-Projekte gab es im September jede Menge Raum und Zeit, einander kennenzulernen und gemeinsam neue Ideen zu entwickeln. Dies wurde vor allem beim abendlich stattfindenden Projekt „<a href="https://de.wikipedia.org/wiki/Wikipedia:Wiki_Loves_Cocktails" target="new">Wiki Loves Cocktails</a>“ ausgiebig getan. Denn nachdem man tolle Artikel und Fotos über leckere Getränke produziert hat, sollten diese ja nicht verschwendet werden.
				</p>
				<blockquote class="quote">
					<div class="quote__body">
						<p class="t--beta">
							„Die Wiki Con ist die perfekte Möglichkeit, andere Mitwirkende zu treffen, die ich lange Jahre nur virtuell kannte.“
						</p>
					</div>
					<footer class="quote__author t--gamma">
						WikiCon-Teilnehmer
					</footer>
				</blockquote>
				<p>
					2016 hat das Organisationsteam das Konzept der Konferenz jedoch noch etwas weiter gedacht: Was bisher ausschließlich ein Treffen aktiver Freiwilliger war, hieß nun auch interessierte Neulinge willkommen, um sich in einem öffentlichen Teil der Konferenz über Wikipedia und die anderen Wikimedia-Projekte zu informieren. Außerdem gab es erstmals Vorträge von Externen aus der Bildungspolitik und Wissenschaft sowie von der Open Knowledge Foundation Deutschland und dem Projekt OpenStreetMap.
				</p>
				<p>
					Keine WikiCon war größer und erfolgreicher als die 2016er Ausgabe mit 333 Teilnehmenden im baden­-württembergischen Kornwestheim. Sie hat es geschafft, viel Wertschätzung für das ehrenamtliche ­Engagement zu vermitteln: Für 93 Prozent der Befragten der abschließenden <a href="https://de.wikipedia.org/wiki/Wikipedia:WikiCon_2016/Evaluation" target="new">Umfrage</a> hat die Konferenz ihre Motivation, zur Wikipedia und ihren Schwesterprojekten beizutragen, weiter gestärkt.
				</p>
				<p>
					Wir freuen uns auf <a href="https://de.wikipedia.org/wiki/Wikipedia:WikiCon_2017" target="new">die nächste WikiCon</a> vom 08. bis 10. September in Leipzig.
				</p>
