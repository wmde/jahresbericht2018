		<div class="limit--s">
			<h1 class="section__title h--beta">
				<em>Regionalisierung</em><br>Lokale Räume geben Wikimedia-Projekten ein Gesicht
			</h1>
			<div class="section__teaser t--gamma">
				Viele Ehrenamtliche der Wikimedia-Projekte wollen auch ­außerhalb der digitalen Welt zusammenkommen. An verschiedenen Orten in Deutschland finden daher bereits seit 2003 regelmäßig <a href="https://de.wikipedia.org/wiki/Wikipedia:Treffen_der_Wikipedianer" target="new">Wikipedia-Stammtische</a> und Editier-Treffen statt. Einige Städte haben inzwischen sogar von Ehrenamtlichen selbstorganisierte Büros, in denen sich Freiwillige zum gemeinsamen Bearbeiten oder für Veranstaltungen treffen können.
			</div>
			<div class="section__body t--gamma-serif">
				<p>
					Freiwillige in <a href="https://de.wikipedia.org/wiki/Wikipedia:WikiProjekt_Bremen/BremenpediA/vor_Ort" target="new">Bremen</a>, <a href="https://de.wikipedia.org/wiki/Wikipedia:Kontor_Hamburg" target="new">Hamburg</a>, <a href="https://de.wikipedia.org/wiki/Wikipedia:Wikipedia-B%C3%BCro_Hannover" target="new">Hannover</a>, <a href="https://de.wikipedia.org/wiki/Wikipedia:Lokal_K" target="new">Köln</a> und <a href="https://de.wikipedia.org/wiki/Wikipedia:WikiMUC" target="new">München</a> (sowie ab 2017 auch in Berlin) kommen in den lokalen Räumen zusammen, um gemeinsame Projekte zu planen und als Community vor Ort zusammenzuwachsen. Gleichzeitig tauschen sie sich dort mit anderen Organisationen mit ähnlichem Schwerpunkt aus und stehen für Kooperationen mit regional ansässigen Institutionen zur Verfügung. Dabei entstehen immer wieder neue Lösungen, Projekte und Ideen, die sie selbst oder mit Unterstützung von Wikimedia Deutschland umsetzen. Der im Frühjahr 2016 eröffnete Raum <a href="https://de.wikipedia.org/wiki/Wikipedia:WikiMUC" target="new">WikiMUC</a> in München arbeitet bereits auf regionaler Ebene mit Serlo (freie Lernplattform), Kiron (Hochschulbildung für Geflüchtete) und Start2Code (Kinder lernen programmieren) zusammen.
				</p>
				<p>
					Wikimedia Deutschland unterstützt das weltweit einzigartige Konzept der lokalen Räume als logistischer und finanzieller Partner. Dazu gehören Vertragsverhandlungen und Zahlungen von Miet-, Neben- und Betriebskosten sowie Finanzmittel für Ausstattung, Veranstaltungen und Öffentlichkeitsarbeit. Die Ehrenamtlichen können sich so ganz auf ihre Aktivitäten konzentrieren. Mit ihnen tauscht sich Wikimedia Deutschland regelmäßig über die lokale Arbeit bei Veranstaltungen aus und unterstützt sie bei der Auswertung. Insgesamt 489 Aktivitäten wurden 2016 in den lokalen Räumen durchgeführt. Darunter waren viele Veranstaltungen, die Freiwillige zusammenbringen, über die Wikimedia-Projekte informieren oder aktiv zu diesen beitragen.
				</p>
