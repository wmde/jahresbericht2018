					Wikimedia Deutschland möchte dazu beitragen, dass Wikipedia & Co. weiterhin Spaß machen. Daher befragen wir die Autorinnen und Autoren regelmäßig, was für sie die wichtigsten fehlenden oder zu verbessernden Funktionen sind. Die Community diskutiert und priorisiert ihre „Technischen Wünsche“, das Team bei Wikimedia Deutschland schaut, welche machbar sind und setzt diese dann um. 2016 war ein unglaublich produktives Jahr für das Projekt, denn viele technische Neuheiten konnten umgesetzt werden. Ein großer Erfolg war die Bereitstellung des <a href="https://mediawiki.org/wiki/Extension:RevisionSlider/de" target="new">„Versionsblätterers“</a>, mit Hilfe dessen in der Wikipedia zwischen verschiedenen Versionen eines Artikels navigiert werden kann. Gemeinschaftlich entstandene Werkzeuge wie dieses oder das ebenfalls entwickelte <a href="http://tools.wmflabs.org/pageviews" target="new">Analyse-Tool</a>, das die Aufrufe von Wikipedia-­Artikeln anzeigt, stehen dann den Wikimedia-Projekten in allen Sprachen zur Verfügung. Der kollaborative Charakter des Projekts Technische Wunschliste unterstützt die gemeinschaftliche Produktion von Freiem Wissen.
