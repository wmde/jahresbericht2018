		<div class="limit--s">
			<h2 class="h--gamma">Revenues</h2>
			<p class="t--gamma-serif finance__p">
				According to accounting, revenues increased to EUR 8.9 million in 2018 (compared with EUR 6.8 million in the previous year). Revenues from membership fees increased significantly to EUR 3.2 million (previous year: EUR 2.5 million), as the number of members rose to 65,439 as of December 31 (previous year: 52,650). Although donation revenues dropped nominally to EUR 1.1 million (previous year: EUR 2.3 million), other revenues increased to EUR 4.5 million (previous year: EUR 1.9 million). This was mainly due to the fact that an inheritance of EUR 1.9 million from 2013 was not recognised in the income statement until 2018 due to previous legal uncertainty.
			</p>
			<h2 class="h--gamma">Expenditures</h2>
			<p class="t--gamma-serif finance__p">
				Expenditures increased to EUR 8.9 million (previous year: EUR 6.8 million). Personnel costs increased by EUR 4.4 million (previous year: EUR 3.5 million). Operational costs also rose to EUR 4.4 million (previous year: EUR 3.2 million), of which EUR 0.8 million are costs related to the aforementioned inheritance, including payments to a legatee.</br>
				The summary "Use of funds" on page 27 of this report provides more detailed information on the use of funds.
			</p>
			<h2 class="h--gamma">Unused Donations / Reserves</h2>
			<p class="t--gamma-serif finance__p">
				Wikimedia Deutschland applies the HFA 21 accounting standard. A special feature of this standard is that revenue from donations is not recognized in the income statement until the donations are made. Therefore, the item " Unused donations" on the liabilities side of the balance sheet is an important benchmark for assessing the financial situation of Wikimedia Deutschland. This amount increased to EUR 7.3 million (previous year: EUR 5.0 million). The total is shown in full as a tax reserve in accordance with §62 AO (German Fiscal Code).
			</p>
			<h2 class="h--gamma">Gemeinnützige Wikimedia Fördergesellschaft mbH (WMFG)</h2>
			<p class="t--gamma-serif finance__p">
				The organization has a wholly owned subsidiary whose exclusive purpose is to procure donations. Donation revenue rose to 10.9 million euros (previous year: 10.3 million euros). Personnel and material expenditures rose slightly to EUR 0.8 million (previous year: EUR 0.7 million). This allowed 8.5 million Euro (previous year: 8.4 million Euro) could be passed on to the Wikimedia Foundation in the USA and 2.4 million Euro (previous year: 1.9 million Euro) to the organization in Germany. The surplus for the year of EUR 0.1 million (previous year: EUR 0.2 million) will be transferred to the reserves.
			</p>
			<h2 class="h--gamma">Use Of Funds</h2>
			<p class="t--gamma-serif finance__p">
				Wikimedia Deutschland, like many fundraising organizations, strives to ensure that funds are maximized to benefit substantive work and that costs for indirect project expenses such as for general public relations, advertising and administration are minimized. Nevertheless, the latter are necessary and appropriate as they organise the use of funds and provide accountability and transparency. The expenses for general public relations and advertising of Wikimedia Deutschland and Gemeinnütziger Wikimedia Fördergesellschaft amounted to 1.6 million Euro in 2018. The administrative expenses amounted to 2.8 million Euro. After deducting the expenses for the aforementioned inheritance, which are regarded as an extraordinary item, they amounted to EUR 2.0 million. From this perspective, the share of administrative costs and costs for advertising and general public relations for both organizations was 20.4 percent.
			</p>
			<h2 class="h--gamma">Auditing</h2>
			<p class="t--gamma-serif finance__p">
				Wikimedia Deutschland voluntarily allows audits of internal processes, the allocation of donations as well as the assets, earnings and financial situation. The audit of Wikimedia Deutschland - Gesellschaft zur Förderung Freien Wissens e. V. and Gemeinnützige Wikimedia Fördergesellschaft mbH was conducted by KWP Revision GmbH Wirtschaftsprüfungsgesellschaft Berlin in December 2018 and March 2019. An unqualified audit opinion was issued. Accordingly, the accounts of the <a href="https://commons.wikimedia.org/wiki/File:2018_JAP_wmfg_Bestätigungsvermerk_Englisch.pdf" target="new">organization</a> and the <a href="https://commons.wikimedia.org/wiki/File:2018_JAP_wmde_Bestätigungsvermerk_Englisch.pdf" target="new">Fördergesellschaft</a> were kept properly and with due diligence, and the audit did not lead to any objections.
			</p>
