					Über 190 Millionen Bearbeitungen von rund 8000 aktiven ehrenamtlichen Editorinnen und Editoren aus aller Welt verzeichnete die freie Wissensdatenbank allein im Jahr 2017. Zum Vergleich: Im selben Zeitraum haben weltweit knapp 71.000 aktive Wikipedianerinnen und Wikipedianer rund 130 Millionen Bearbeitungen in sämtlichen fast 300 Wikipedia Sprachversionen getätigt. Damit ist Wikidata aktuell eines der am schnellsten wachsenden Wissensprojekte im gesamten Wikimedia-Universum.
