				Wikipedia und Wikimedia
			</p>
			<div class="section__body t--gamma-serif">
				<p>
					Die deutsche Version der freien Online-Enzyklopädie Wikipedia ging im März 2001 online und wird ausschließlich von ehrenamtlichen Autorinnen und Autoren erstellt. Sie schreiben Artikel, halten Informationen aktuell, verbessern Fehler und teilen so ihr Wissen mit der gesamten Welt. Der Verein Wikimedia Deutschland wurde 2004 von Wikipedia-Aktiven gegründet. Die Unterstützung der Wikipedia-Autorenschaft ist seitdem ein fester Bestandteil der Arbeit von Wikimedia Deutschland. Als Gesellschaft zur Förderung Freien Wissens tun wir aber noch viel mehr: Wir fördern die ehrenamtlich Aktiven in sämtlichen Wikimedia-Projekten, entwickeln freie Software und Technik, setzen uns für offene Wissenschaft, Bildung und Kultur ein und stärken politische Rahmenbedingungen, die den freien Zugang zu Wissen ermöglichen.
