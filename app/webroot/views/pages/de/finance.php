	<section class="section section__body cp">
		<div class="limit--s finance__intro">
			<h1 class="h--beta finance__intro-heading">Finanzen</h1>
			<p class="t--gamma">2018 war für Wikimedia Deutschland aus finanzieller Sicht ein sehr gutes Jahr.</p>
		</div>
		<div class="limit--m finance__boxes">
			<a class="border-box border-box--finance t--epsilon" href="<?= $url('finance/assn', $lang) ?>">
				<span>Zum Finanzteil <em class="t--delta">Wikimedia Deutschland e.&nbsp;V.</em></span>
			</a>
			<a class="border-box border-box--finance t--epsilon" href="<?= $url('finance/fund', $lang) ?>">
				<span>Zum Finanzteil <em class="t--delta">Gemeinnützige Wikimedia Fördergesellschaft mbH</em></span>
			</a>
		</div>
		<div class="limit--s">
			<h2 class="h--gamma">Erträge</h2>
			<p class="t--gamma-serif finance__p">
				Nach Rechnungslegung ist die Höhe der Erträge 2018 auf 8,9 Millionen Euro gestiegen (zum Vergleich: im Vorjahr waren es 6,8 Mio. Euro). Deutlich gesteigert haben sich die Erträge aus Mitgliedsbeiträgen auf 3,2 Mio. Euro (Vorjahr: 2,5 Mio. Euro), da die Zahl der Mitglieder mit Stand vom 31. Dezember auf 65.439 (Vorjahr: 52.650) stieg. Die Spendenerträge sind zwar nominal auf 1,1 Mio. Euro (Vorjahr 2,3 Mio. Euro) zurückgegangen, allerdings sind die übrigen Erlöse auf 4,5 Mio. Euro (Vorjahr 1,9 Mio. Euro) gestiegen. Wesentliche Ursache hierfür war die aufgrund vorheriger Rechtsunsicherheit erst in 2018 erfolgswirksame Verbuchung einer Erbschaft aus dem Jahr 2013 in Höhe von 1,9 Mio. Euro.
			</p>
			<h2 class="h--gamma">Aufwendungen</h2>
			<p class="t--gamma-serif finance__p">
				Die Höhe der Aufwendungen stieg auf 8,9 Mio. Euro (Vorjahr 6,8 Mio. Euro). Steigerungen gab es bei den Personalaufwendungen in Höhe von 4,4 Mio. Euro (Vorjahr: 3,5 Mio. Euro). Auch bei den betrieblichen Aufwendungen ist ein Anstieg auf 4,4 Mio. Euro (Vorjahr 3,2 Mio. Euro) zu verzeichnen, wobei davon 0,8 Mio. Euro Aufwendungen im Zusammenhang mit der erwähnten Erbschaft sind, darunter Zahlungen an eine Vermächtnisnehmerin.</br>
				Aus der Übersicht „<a href="#mittelverwendung">Mittelverwendung</a>“ wird die inhaltliche Verwendung der Mittel genauer ersichtlich.
			</p>
			<h2 class="h--gamma">Noch nicht verbrauchte Spendenmittel / Rücklagen</h2>
			<p class="t--gamma-serif finance__p">
				Wikimedia Deutschland wendet als Rechnungslegungsstandard HFA 21 an. Eine Besonderheit dieses Standards ist dabei, dass Erträge aus Spenden erst zum Zeitpunkt der Verausgabung der Spenden erfolgswirksam verbucht werden. Daher ist die Position „noch nicht verbrauchte Spendenmittel“ auf der Passivseite der Bilanz ein wichtiger Maßstab zur Beurteilung der Finanzsituation von Wikimedia Deutschland. Dieser Betrag ist auf 7,3 Mio. Euro (Vorjahr: 5,0 Mio. Euro) gestiegen. Die Summe wird in voller Höhe als steuerliche Rücklage nach §62 AO ausgewiesen.
			</p>
			<h2 class="h--gamma">Mittelverwendung</h2>
			<p class="t--gamma-serif finance__p">
				Der Verein hat eine 100%-ige Tochter, deren ausschließlicher Zweck die Beschaffung von Spendengeldern zur Weiterleitung ist. Der Spendenertrag stieg auf 10,9 Mio. Euro (Vorjahr: 10,3 Mio. Euro). Die Personal- und Sachaufwendungen stiegen leicht auf 0,8 Mio. Euro (Vorjahr: 0,7 Mio. Euro). Damit konnten 8,5 Mio. Euro (Vorjahr: 8,4 Mio. Euro) an die Wikimedia Foundation in den USA und 2,4 Mio. Euro (Vorjahr: 1,9 Mio. Euro) an den Verein in Deutschland weitergeleitet werden. Der Jahresüberschuss von 0,1 Mio. Euro (Vorjahr: 0,2 Mio. Euro) wird in die Gewinnrücklage eingestellt.
			</p>
			<h2 class="h--gamma">Mittelverwendung</h2>
			<p class="t--gamma-serif finance__p">
				Wikimedia Deutschland ist wie viele Spenden sammelnde Organisationen bemüht, dass möglichst viel Geld der inhaltlichen Arbeit zugute kommt und möglichst wenig Kosten für allgemeine Öffentlichkeitsarbeit, Werbung und Verwaltung anfallen. Gleichwohl sind letztere notwendig und sinnvoll, da sie die Mittelverwendung organisieren sowie Rechenschaft und damit Transparenz ermöglichen. Die Kosten für allgemeine Öffentlichkeitsarbeit und Werbung von Wikimedia Deutschland und Gemeinnütziger Wikimedia Fördergesellschaft betrugen 1,6 Mio. Euro im Jahr 2018. Die Verwaltungskosten betrugen 2,8 Mio. Euro. Abzüglich der als Sonderfaktor zu betrachteten Aufwendungen für die bereits erwähnte Erbschaft betrugen sie 2,0 Mio Euro. Bei dieser Betrachtungsweise lag der Anteil von Verwaltungskosten und Kosten für Werbung und Allgemeine Öffentlichkeitsarbeit beider Gesellschaften bei 20,4 Prozent.
			</p>
			<h2 class="h--gamma">Wirtschaftsprüfung</h2>
			<p class="t--gamma-serif finance__p">
				Wikimedia Deutschland lässt interne Abläufe, die Spendenverteilung sowie die Vermögens-, Ertrags-, und Finanzlage freiwillig prüfen. Die Prüfung des Wikimedia Deutschland - Gesellschaft zur Förderung Freien Wissens e. V. und der Gemeinnützigen Wikimedia Fördergesellschaft mbH erfolgte durch die KWP Revision GmbH Wirtschaftsprüfungsgesellschaft Berlin und wurde im Dezember 2018 und März 2019 durchgeführt. Es wurde ein uneingeschränkter Bestätigungsvermerk erteilt. Demnach wurde die Buchführung des <a href="https://commons.wikimedia.org/wiki/File:Bestätigungsvermerk_WMDE_2018.pdf" target="new">Vereins</a> und der <a href="https://commons.wikimedia.org/wiki/File:Bestätigungsvermerk_WMFG_2018.pdf" target="new">Gesellschaft</a> vollständig und gewissenhaft vorgenommen und die Prüfung hat insgesamt zu keinerlei Einwänden geführt.
			</p>
		</div>
	</section>
